int main(void)
{
  for (unsigned i=0; i<arr.size(); ++i) {
    unsigned y = i+1;
    auto d = gi::easter_date(y) ;
    if (d != arr[i]) {
      std::cout << "easter_date("<< y <<") test failed with values: \n" << d << " != " << arr[i] << '\n' ;
      return -1;
    }
    if (!gi::is_date_of(y, arr[i], gi::PASHA)) {
      std::cout << "is_date_of("<< y << ',' << arr[i] <<",PASHA) test failed \n" ;
      return -1;
    }
  }
  return 0;
}
